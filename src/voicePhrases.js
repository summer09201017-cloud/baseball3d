// 播報詞庫(固定句,全部預烤 mp3)+key 函式——gen-voice.mjs 與 runtime voice.js 共用,
// 字串差一個字就對不上,新增句子改這裡再跑 node scripts/gen-voice.mjs。
export function voiceKey(text) {
  // FNV-1a(同 paul ttsKey 思路;去空白)
  const s = String(text).replace(/\s+/g, "");
  let h = 0x811c9dc5;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = (h * 0x01000193) >>> 0;
  }
  return h.toString(36);
}

